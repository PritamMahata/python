# find largest and smallest number from a list and tupple
list = [1, 31, 31, 31, 24235, 7, 5, 3, 3, 7, 8, 2]
tupple = (31, 31, 31, 235, 7, 5, 8)

print("largest number from list is ", max(list))
print("smallest number from list is ", min(list))
print("largest number from tupple is ", max(tupple))
print("smallest number from tupple is ", min(tupple))
